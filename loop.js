// Print numbers from 1 to 10 and check if each is even or odd
for (let i = 1; i <= 10; i++) {
    if (i % 2 === 0) {
        console.log(i + " is an even number");
    } else {
        console.log(i + " is an odd number");
    }
}

// After the loop, print a message
console.log("Loop finished!");
